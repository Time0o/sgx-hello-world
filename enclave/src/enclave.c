#include "sgx_tkey_exchange.h"

#include "enclave_t.h"

sgx_status_t enclave_ra_init(sgx_ra_context_t *ra_context)
{
    /* The server public key is hardcoded into the enclave to ensure that
     * communication is only possible with the intended remote service. */
    static sgx_ec256_public_t server_ec256_public = {
        {
            0xe1, 0x14, 0x89, 0xf5, 0xa5, 0xa9, 0xbe, 0x8b,
            0x0d, 0x3f, 0xbc, 0xc2, 0x95, 0x44, 0xf3, 0x28,
            0x4d, 0x39, 0xcd, 0xfd, 0xdb, 0x93, 0xc9, 0x3b,
            0x5d, 0xe4, 0x16, 0xd9, 0xc9, 0xc3, 0xe9, 0xb3
        },
        {
            0x00, 0xc5, 0xfb, 0x36, 0x70, 0xc3, 0xc7, 0x27,
            0x1b, 0x30, 0x65, 0x05, 0x2e, 0x7c, 0xd6, 0xa9,
            0xee, 0xe2, 0x63, 0xe8, 0x8c, 0x74, 0x1c, 0x13,
            0xcc, 0x3a, 0x75, 0xb8, 0xd6, 0xcf, 0x74, 0x0e
        }
    };

    return sgx_ra_init(&server_ec256_public, 0, ra_context);
}

sgx_status_t enclave_ra_close(sgx_ra_context_t ra_context)
{
    return sgx_ra_close(ra_context);
}
